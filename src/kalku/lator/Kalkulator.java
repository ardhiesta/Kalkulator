package kalku.lator;

/**
 *
 * @author linuxluv
 */
public class Kalkulator {
    double tambahkanBilangan(double bil1, double bil2){
        double hasil = 0.0;
        hasil = bil1 + bil2;
        return hasil;
    }
    
    double kurangiBilangan(double bil1, double bil2){
        double hasil = 0.0;
        hasil = bil1 - bil2;
        return hasil;
    }
}
